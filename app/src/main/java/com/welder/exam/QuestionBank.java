    private final List<Question> questions = new ArrayList<>();

    public QuestionBank() {
        for (String q : QUESTIONS) {
            String[] parts = q.split("\\|");
            if (parts.length >= 3) {
                questions.add(new Question(parts[0].trim(), parts[1].trim(), parts[2].trim()));
            }
        }
    }

    public int size() { return questions.size(); }

    public static class Question {
        public final String keyword;
        public final String answer;
        public final String type;

        public Question(String keyword, String answer, String type) {
            this.keyword = keyword;
            this.answer = answer;
            this.type = type;
        }
    }

    public static class MatchResult {
        public final String answer;
        public final String type;
        public final float score;

        public MatchResult(String answer, String type, float score) {
            this.answer = answer;
            this.type = type;
            this.score = score;
        }
    }

    public MatchResult findAnswer(String questionText) {
        if (questionText == null || questionText.isEmpty()) return null;

        String normalized = normalize(questionText);
        Question best = null;
        float bestScore = 0f;

        for (Question q : questions) {
            String kwNorm = normalize(q.keyword);
            float score = similarity(normalized, kwNorm);

            if (kwNorm.length() > 3 && normalized.length() > 3) {
                int prefixLen = 0;
                int minLen = Math.min(normalized.length(), kwNorm.length());
                for (int i = 0; i < minLen; i++) {
                    if (normalized.charAt(i) == kwNorm.charAt(i)) prefixLen++;
                    else break;
                }
                float prefixScore = (float) prefixLen / Math.max(normalized.length(), kwNorm.length());
                score = Math.max(score, prefixScore * 0.9f);
            }

            if (kwNorm.contains(normalized) || normalized.contains(kwNorm)) {
                score = Math.max(score, 0.85f);
            }

            if (score > bestScore) {
                bestScore = score;
                best = q;
            }
        }

        if (best != null && bestScore >= 0.3f) {
            return new MatchResult(best.answer, best.type, bestScore);
        }
        return null;
    }

    private String normalize(String text) {
        return text.toLowerCase()
            .replaceAll("[\\s　]+", "")
            .replaceAll("[①②③④⑤⑥⑦⑧⑨⑩]", "")
            .replaceAll("[ABCD】△▽○●◆■□★☆※]", "")
            .replaceAll("[，。、：；？！\"\"''（）【】《》]", "");
    }

    private float similarity(String a, String b) {
        if (a.equals(b)) return 1f;
        if (a.isEmpty() || b.isEmpty()) return 0f;

        int[][] dp = new int[a.length() + 1][b.length() + 1];
        for (int i = 1; i <= a.length(); i++) {
            for (int j = 1; j <= b.length(); j++) {
                if (a.charAt(i - 1) == b.charAt(j - 1)) {
                    dp[i][j] = dp[i - 1][j - 1] + 1;
                } else {
                    dp[i][j] = Math.max(dp[i - 1][j], dp[i][j - 1]);
                }
            }
        }
        int lcs = dp[a.length()][b.length()];
        return (float) (2 * lcs) / (a.length() + b.length());
    }
